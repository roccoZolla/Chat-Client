    private void sendMessageToServer() {
        if (!chat_field.getText().isEmpty()) {
            // invia messaggio al server
            messageSender.sendMessage(chat_field.getText());
            chat_area.append("Tu: " + chat_field.getText() + "\n");
            chat_field.setText("");
        }
    }

